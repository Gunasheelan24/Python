# In Python, input() is a built-in function that allows you to accept user input from the 
# console (also known as standard input). When you call input(), 
# the program waits for the user to type something and press Enter. Whatever the user types is then returned as a string.


# it is a prompt in javascript 

get_age = input("What your age?")

# Normal concatenation  
print("Your Age Is " + get_age)

# formated string literals

isgood_guy = input("Is he a good guy? ")
print(f"he is a {isgood_guy} guy")


